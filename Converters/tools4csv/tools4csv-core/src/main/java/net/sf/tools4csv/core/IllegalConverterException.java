package net.sf.tools4csv.core;

public class IllegalConverterException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 361252638193897823L;

	public IllegalConverterException() {
		super();
	}

	public IllegalConverterException(String message, Throwable cause) {
		super(message, cause);
	}

	public IllegalConverterException(String message) {
		super(message);
	}

	public IllegalConverterException(Throwable cause) {
		super(cause);
	}

}
