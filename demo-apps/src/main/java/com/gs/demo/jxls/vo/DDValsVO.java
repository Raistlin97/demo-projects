package com.gs.demo.jxls.vo;

import java.io.Serializable;

public class DDValsVO implements Serializable {

	
	/**
	 * 
	 */
	private static final long serialVersionUID = -324774491827941391L;
	
	private String code, value;

	/**
	 * @return the code
	 */
	public String getCode() {
		return code;
	}

	/**
	 * @param code the code to set
	 */
	public void setCode(String code) {
		this.code = code;
	}

	/**
	 * @return the value
	 */
	public String getValue() {
		return value;
	}

	/**
	 * @param value the value to set
	 */
	public void setValue(String value) {
		this.value = value;
	}
	
	@Override
	public String toString() {
		return "\"" + 
			formatToColumnName(((null != code) ? code.trim() : ""))
		+ "\", \"" + ((null != value) ? value.trim() : "") + "\"";
	}
	public static String formatToColumnName(String txt){
		return txt.toUpperCase().replaceAll(" ", "_").replaceAll("-", "");
	}
	/* (non-Javadoc)
	 * @see java.lang.Object#hashCode()
	 */
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((code == null) ? 0 : code.hashCode());
		return result;
	}

	/* (non-Javadoc)
	 * @see java.lang.Object#equals(java.lang.Object)
	 */
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		DDValsVO other = (DDValsVO) obj;
		if (code == null) {
			if (other.code != null)
				return false;
		} else if (!code.equals(other.code))
			return false;
		return true;
	}
	
	
}
