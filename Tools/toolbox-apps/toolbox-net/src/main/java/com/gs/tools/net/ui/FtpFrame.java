    private void addInternalFrame(JInternalFrame internalFrame){
        JInternalFrame[] iframes = mainDesktopPane.getAllFrames();
        // Some frames added
        if(null != iframes && iframes.length > 0){
            JInternalFrame lastFrame = iframes[iframes.length - 1];
            if(null != lastFrame){
                mainDesktopPane.add(internalFrame);
                internalFrame.setBounds(
                        lastFrame.getX() + 10,
                        lastFrame.getY()+10,
                        internalFrame.getWidth(),
                        internalFrame.getHeight());
            }
        } else {
            mainDesktopPane.add(internalFrame);
                internalFrame.setBounds(0, 0,
                        internalFrame.getWidth(),
                        internalFrame.getHeight());
           
        }
        mainDesktopPane.setSelectedFrame(internalFrame);
    }

