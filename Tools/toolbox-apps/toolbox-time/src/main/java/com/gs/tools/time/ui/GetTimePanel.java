    private void convertButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_convertButtonActionPerformed
        String miliStr = mileSecTextField.getText();
        String formatStr = formatStringTextField.getText();
        Long mili = System.currentTimeMillis();
        if(StringUtil.hasValidContent(miliStr)){
            mili = Long.valueOf(miliStr);
        }

        if(!StringUtil.hasValidContent(formatStr)){
            formatStr = "dd-MM-yyyy hh:mm:ss aa";
        }

        TimeZone timeZone = TimeZone.getDefault();
        if(null != timezoneComboBox.getSelectedItem()){
            TimeZoneData tzd = (TimeZoneData) timezoneComboBox.getSelectedItem();
            timeZone = tzd.getTimeZone();
        }

        SimpleDateFormat dateFormat = new SimpleDateFormat(formatStr);
        Calendar c = GregorianCalendar.getInstance();
        c.setTimeZone(timeZone);
	c.setTimeInMillis(mili);
        convertTextArea.setText(dateFormat.format(c.getTime()));
    }//GEN-LAST:event_convertButtonActionPerformed

