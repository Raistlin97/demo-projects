    private void leftRootComboBoxPropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_leftRootComboBoxPropertyChange
        if(!leftRootComboBox.equals(evt.getSource())){
        	return;
        }
    	if(leftRootComboBox.getSelectedIndex() >= 0){
        	String rootName = (String) leftRootComboBox.getSelectedItem();
        	File root = new File(rootName);
        	FileSystemTreeModel treeModel = new FileSystemTreeModel(root);
        	leftDirectoryTree.setModel(treeModel);
        	leftDirectoryTree.updateUI();
        }
    }//GEN-LAST:event_leftRootComboBoxPropertyChange

