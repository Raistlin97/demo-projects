    private void browseInputXmlButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_browseInputXmlButtonActionPerformed
        JFileChooser chooser = new JFileChooser(".");
        chooser.setMultiSelectionEnabled(false);
        chooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
        ExtensionFileFilter filter = new ExtensionFileFilter(
                new String[]{"xml"}, "Xml files only");
        chooser.setFileFilter(filter);
        int opt = chooser.showOpenDialog(this);
        if (opt == JFileChooser.APPROVE_OPTION) {
            final File f = chooser.getSelectedFile();
            inputFileName = f.getAbsolutePath();
            inputFileNameTextField.setText(inputFileName);
            inputPayloadTextArea.setText(FileRWUtil.readAsText(f));
        }
    }//GEN-LAST:event_browseInputXmlButtonActionPerformed

