    private void invokeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_invokeButtonActionPerformed
        
        //Thread t = new Thread(r);
        if(!running){
            
            Runnable r = new Runnable() {

                public void run() {
                    invokeButton.setText("Stop");
                    invokeButton.setIcon(new javax.swing.ImageIcon(
                        getClass().getResource("/com/gs/ws/images/stopped.gif")));
                    clearAllinputButton.setEnabled(false);
                    invokeProgressBar.setVisible(true);
                    for (int i = 0; i < 100; i++) {
                        MainLogger.logInfo("Count : " + (i+1));
                        try {
                            Thread.sleep(50);
                        } catch (InterruptedException ex) {
                            Logger.getLogger(JWSInvokerFrame.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
                        }
                    }
                    payloadOutputTextArea.setText("Output payload....");
                    invokeProgressBar.setVisible(false);
                    invokeButton.setText("Invoke");
                    invokeButton.setIcon(new javax.swing.ImageIcon(
                        getClass().getResource("/com/gs/ws/images/playing.gif")));
                    clearAllinputButton.setEnabled(true);
                    running = false;
                }
            };
            ttt = new Thread(r);
            ttt.start();
            running = true;
        }else{
            ttt.stop();
            running = false;
            invokeButton.setText("Invoke");
            invokeButton.setIcon(new javax.swing.ImageIcon(
                    getClass().getResource("/com/gs/ws/images/playing.gif")));
            invokeProgressBar.setVisible(false);
            clearAllinputButton.setEnabled(true);
        }
    }//GEN-LAST:event_invokeButtonActionPerformed


